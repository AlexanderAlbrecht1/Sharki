/**
 * Represents the state of keyboard inputs for controlling the game.
 * Each property corresponds to a specific key and indicates whether it is currently pressed.
 */
class Keyboard {
    /**
     * @type {boolean} Indicates if the LEFT arrow key is pressed.
     */
    LEFT = false;

    /**
     * @type {boolean} Indicates if the RIGHT arrow key is pressed.
     */
    RIGHT = false;

    /**
     * @type {boolean} Indicates if the UP arrow key is pressed.
     */
    UP = false;

    /**
     * @type {boolean} Indicates if the DOWN arrow key is pressed.
     */
    DOWN = false;

    /**
     * @type {boolean} Indicates if the SPACE key is pressed.
     */
    SPACE = false;

    /**
     * @type {boolean} Indicates if the 'Q' key is pressed.
     */
    Q = false;

    /**
     * @type {boolean} Indicates if the 'E' key is pressed.
     */
    E = false;
}
